package controller;

import com.opensymphony.xwork2.ActionSupport;
import java.util.Date;
import model.Feedback;

/**
 *
 * @author Administrator
 */
public class talktous extends ActionSupport {

    private String email;
    private String feedback;
    private spDAO myDao;
    private String name;
    private String subject;
    private String phone;
    private Date date;

    @Override
    public String execute() throws Exception {
        date = new Date();
        Feedback fback = new Feedback(name, email, feedback);
        fback.setSubject(subject);
        fback.setPhone(phone);
        fback.setSugDate(getDate());
        myDao.getDbsession().saveOrUpdate(fback);
        addActionMessage("\t\t\t           Thanks for your Interest we will get back to you soon");
        this.name = null;
        this.email = null;
        this.feedback = null;
        return "success";
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the myDao
     */
    public spDAO getMyDao() {
        return myDao;
    }

    /**
     * @param myDao the myDao to set
     */
    public void setMyDao(spDAO myDao) {
        this.myDao = myDao;
    }

    /**
     * @return the feedback
     */
    public String getFeedback() {
        return feedback;
    }

    /**
     * @param feedback the feedback to set
     */
    public void setFeedback(String feedback) {
        this.feedback = feedback;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the subject
     */
    public String getSubject() {
        return subject;
    }

    /**
     * @param subject the subject to set
     */
    public void setSubject(String subject) {
        this.subject = subject;
    }

    /**
     * @return the phone
     */
    public String getPhone() {
        return phone;
    }

    /**
     * @param phone the phone to set
     */
    public void setPhone(String phone) {
        this.phone = phone;
    }

    /**
     * @return the date
     */
    public Date getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(Date date) {
        this.date = date;
    }
}
